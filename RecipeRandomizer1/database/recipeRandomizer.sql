DROP TABLE IF EXISTS recipe, food_category, ingredients, measurements, recipe_steps, recipe_ingredient CASCADE;
DROP SEQUENCE IF EXISTS recipe_serial, food_category_serial, ingredient_serial, measurement_serial, recipe_steps_serial;

CREATE SEQUENCE food_category_serial;
CREATE TABLE food_category (
	food_category_id int NOT NULL DEFAULT nextval('food_category_serial'),
	food_category_name varchar(200) NOT NULL,
	CONSTRAINT PK_food_category_id PRIMARY KEY(food_category_id)
);

CREATE SEQUENCE recipe_serial;
CREATE TABLE recipe (
	recipe_id int NOT NULL DEFAULT nextval('recipe_serial'),
	food_category_id int NOT NULL,
	recipe_name varchar(200) NOT NULL,
	recipe_description varchar(500) NOT NULL,
	total_time int NOT NULL,
	CONSTRAINT PK_recipe_id PRIMARY KEY(recipe_id),
	CONSTRAINT FK_food_category_id FOREIGN KEY(food_category_id) REFERENCES food_category(food_category_id)
);

CREATE SEQUENCE ingredient_serial;
CREATE TABLE ingredients (
	ingredient_id int NOT NULL DEFAULT nextval('ingredient_serial'),
	ingredient_name varchar(200) NOT NULL,
	CONSTRAINT PK_ingredient_id PRIMARY KEY(ingredient_id)
);

CREATE SEQUENCE measurement_serial;
CREATE TABLE measurements (
	measurement_id int NOT NULL DEFAULT nextval('measurement_serial'),
	measurement_name varchar(200) NOT NULL,
	CONSTRAINT PK_measurement_id PRIMARY KEY(measurement_id)
);

CREATE SEQUENCE recipe_steps_serial;
CREATE TABLE recipe_steps (
	recipe_step_id int NOT NULL DEFAULT nextval('recipe_steps_serial'),
	recipe_id int,
	recipe_step_number int NOT NULL,
	recipe_step_desc varchar(1000) NOT NULL,
	CONSTRAINT PK_recipe_step_id PRIMARY KEY(recipe_step_id),
	CONSTRAINT FL_recipe_id FOREIGN KEY(recipe_id) REFERENCES recipe(recipe_id)
);

CREATE TABLE recipe_ingredient (
	recipe_id int,
	ingredient_id int, 
	measurement_id int, 
	amount int, 
	CONSTRAINT FL_recipe_id FOREIGN KEY(recipe_id) REFERENCES recipe(recipe_id),
	CONSTRAINT FL_ingredient_id FOREIGN KEY(ingredient_id) REFERENCES ingredients(ingredient_id),
	CONSTRAINT FL_measurement_id FOREIGN KEY(measurement_id) REFERENCES measurements(measurement_id)
);

